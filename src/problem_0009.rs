/// problem 9
///
/// find the product of a triplet a, b, c for which
/// * a+b+c=1000
/// * a<b<c
/// * a²+b²=c²
pub fn special_pythagorean_triplet() -> ((u32, u32, u32), u32) {
    unimplemented!();
    let mut triplet: (u32, u32, u32) = (1, 2, 3);

    (triplet, triplet.0*triplet.1*triplet.2)
}
